/** Format a metric value for display based on its format hint. */
function formatMetricValue(metric: MetricItem): string {
  const { value, format, prefix, suffix } = metric

  let formatted: string

  if (format === 'currency') {
    formatted = value.toLocaleString('en-US', {
      minimumFractionDigits: 2,
      maximumFractionDigits: 2,
    })
  } else {
    formatted = value.toLocaleString('en-US')
  }

  return `${prefix ?? ''}${formatted}${suffix ?? ''}`
}

